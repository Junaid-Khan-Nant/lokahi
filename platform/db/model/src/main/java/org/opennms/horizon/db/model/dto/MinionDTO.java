package org.opennms.horizon.db.model.dto;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlAttribute;
import javax.xml.bind.annotation.XmlID;
import javax.xml.bind.annotation.XmlRootElement;
import java.util.Objects;

@XmlRootElement(name="minion")
@XmlAccessorType(XmlAccessType.NONE)
public class MinionDTO {

    @XmlID
    @XmlAttribute(name="id")
    private String id;

    @XmlAttribute(name="label")
    private String label;

    @XmlAttribute(name="location")
    private String location;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (!(o instanceof MinionDTO)) return false;
        MinionDTO minionDTO = (MinionDTO) o;
        return Objects.equals(id, minionDTO.id) && Objects.equals(label, minionDTO.label) && Objects.equals(location, minionDTO.location);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id, label, location);
    }
}
